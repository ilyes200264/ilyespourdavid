export type Language = "fr" | "en"
export type TranslationKey = keyof typeof translations.fr

// -----------------------------------------------------------------------------
// Utility to safely fetch a translation by dot-notation key.
//
// • If the final value is a string   → return it.
// • If the final value is an object → attempt to return its `title` prop
//   (common pattern for nested translation objects). If none exists, return
//   an empty string so we never pass a raw object to React (which causes
//   "Objects are not valid as a React child" errors).
// -----------------------------------------------------------------------------
export function getTranslation(language: Language, key: string): string {
  const segments = key.split(".")
  let value: unknown = translations[language]

  for (const segment of segments) {
    if (value && typeof value === "object" && segment in (value as object)) {
      // @ts-expect-error progressive drilling
      value = value[segment]
    } else {
      return key // missing key -> make it obvious in UI
    }
  }

  if (typeof value === "string") return value

  if (value && typeof value === "object" && "title" in (value as any)) {
    const maybeTitle = (value as any).title
    if (typeof maybeTitle === "string") return maybeTitle
  }

  // Fallback – never let a raw object reach the UI
  return ""
}
